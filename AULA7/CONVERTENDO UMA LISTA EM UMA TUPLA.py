# Cria uma lista com nomes de carros
lista_carros = ["Gol", "Corolla", "Ranger", "Kadett", "Fusca", "Clio"]

# Converte a lista em uma tupla
tupla_carros = tuple(lista_carros)
print(f"Tupla carros: {tupla_carros}")

# Converte a tupla de volta para uma lista
list_carros = list(tupla_carros)
print(f"Lista carros: {list_carros}")